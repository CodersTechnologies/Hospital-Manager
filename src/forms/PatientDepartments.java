    public void printConsultation(){
        try{
            String sql="select * from opd where opd_ID='"+txtIdOPD.getText()+"'";
            JasperDesign jd = JRXmlLoader.load("src/reports/OPD Consultation Report.jrxml");
            JRDesignQuery qry = new JRDesignQuery();
            qry.setText(sql);
            jd.setQuery(qry);

            JasperReport jr = JasperCompileManager.compileReport(jd);
            JasperPrint jp = JasperFillManager.fillReport(jr, null, conn);
            JasperViewer.viewReport(jp, false);
        }
        catch(JRException e){
            JOptionPane.showMessageDialog(null, e,"printConsultation() Error",JOptionPane.ERROR_MESSAGE);
        }
    }
