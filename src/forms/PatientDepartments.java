    //Program to get OPDTableData to field
    public void getOPDTableToField(){
        try{
            int row=tblOPD.getSelectedRow();
            String opdID=tblOPD.getModel().getValueAt(row, 0).toString();
            String sql="Select * from opd where opd_ID='"+opdID+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                txtIdOPD.setText(rs.getString("opd_ID"));
                txtNameOPD.setText(rs.getString("opd_Name"));
                cmbBloodGroupOPD.setSelectedItem(rs.getString("opd_BloodGroup"));
                cmbMaritalOPD.setSelectedItem(rs.getString("opd_MaritalStatus"));
                dtDOBOPD.setDate(rs.getDate("opd_DOB"));
                txtKinsNameOPD.setText(rs.getString("opd_KinsName"));
                txtAddressOPD.setText(rs.getString("opd_Address"));
                txtCityOPD.setText(rs.getString("opd_City"));
                txtStateOPD.setText(rs.getString("opd_State"));
                txtPinOPD.setText(rs.getString("opd_Pin"));
                txtContactOPD.setText(rs.getString("opd_Contact"));
                txtEmailOPD.setText(rs.getString("opd_Email"));
                txtKinContactOPD.setText(rs.getString("opd_KinsContact"));
                cmbDeptOPD.setSelectedItem(rs.getString("opd_ConsultDept"));
                cmbConsultantOPD.setSelectedItem(rs.getString("opd_Consultant"));
                lblDate.setText("In-Date: "+rs.getString("opd_RegDate"));
                lblTime.setText("In-Time: "+rs.getString("opd_RegTime"));
                btnHospitalServices.setEnabled(true);
                btnSaveOPD.setEnabled(false);
                btnUpdateOPD.setEnabled(true);
                btnDeleteOPD.setEnabled(true);
                btnPrintOPDForm.setEnabled(true);
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
