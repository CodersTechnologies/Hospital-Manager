    //Program to read database
    public void readDB(){        
        try{
            String sql="Select * from diagnosticsandtests";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            tblDiagnosAndTest.setModel(DbUtils.resultSetToTableModel(rs));
            pst.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    //Program to read database and set service id
    int count=0;
    public void setServiceID(){
        try{
            String sql="Select * from diagnosticsandtests";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next()){
                count=rs.getInt("Service_ID");
                count++;
            }
            txtServiceID.setText(String.valueOf(count));
            pst.close();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        txtServiceName.setText("");
        txtServiceCost.setText("");
        txtServiceDesc.setText("");
        btnUpdate.setEnabled(false);
        btnDeleteService.setEnabled(false);
        btnAddService.setEnabled(true);
    }
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        readDB();
        setServiceID();
    }//GEN-LAST:event_formWindowOpened
    //Program to insert into database
    public void addToDB(){
        try{
            String sql="insert into diagnosticsandtests(Service_ID,Service_Name,Service_Cost,Service_Description)"
                    + "values(?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1, txtServiceID.getText());
            pst.setString(2, txtServiceName.getText());
            pst.setString(3, txtServiceCost.getText());
            pst.setString(4, txtServiceDesc.getText());
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "New Service Added Successfully","Data Added",JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    private void btnAddServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddServiceActionPerformed
        // TODO add your handling code here:
        if(!"".equals(txtServiceID.getText()) && 
                !"".equals(txtServiceName.getText()) &&
                !"".equals(txtServiceCost.getText()) &&
                !"".equals(txtServiceDesc.getText())){
            addToDB();
            readDB();
            setServiceID();
        }
        else{
            JOptionPane.showMessageDialog(null, "Empty Field Detected");
        }
    }//GEN-LAST:event_btnAddServiceActionPerformed
    //Program to update Diagnostics and Test Services
    public void updtService(){
        try{
            String sql="update diagnosticsandtests set Service_Name='"+txtServiceName.getText()+"',"
                    + "Service_Cost='"+txtServiceCost.getText()+"',Service_Description='"+txtServiceDesc.getText()+"'"
                    + "where Service_ID='"+txtServiceID.getText()+"'";
            pst=conn.prepareStatement(sql);
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "Data Updated Successfully","Data Updated",JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, e);
        }
        readDB();
        setServiceID();
    }
