    //Program to get DiagnosticsAndTest Table to field
    public void getDiagAndTestTableToField(){
        try{
            int row=tblDiagnosAndTest.getSelectedRow();
            String serveID=tblDiagnosAndTest.getModel().getValueAt(row, 0).toString();
            String sql="Select * from diagnosticsandtests where Service_ID='"+serveID+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                txtServiceID.setText(rs.getString("Service_ID"));
                txtServiceName.setText(rs.getString("Service_Name"));
                txtServiceCost.setText(rs.getString("Service_Cost"));
                txtServiceDesc.setText(rs.getString("Service_Description"));
                btnAddService.setEnabled(false);
                btnUpdate.setEnabled(true);
                btnDeleteService.setEnabled(true);                
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }    
